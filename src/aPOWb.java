public class aPOWb {

    public void a_Pow_b(){
        int a=2;
        int b=4;
        int ans=0;
        for(int i=0; i< b; i++){
            for(int j = 0 ; j < a; j++){
               ans = ans + a;
            }
        }
        System.out.println(ans);
    }
}
